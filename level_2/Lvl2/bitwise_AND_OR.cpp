#include <iostream>

using namespace std;

int main()
{
    cout << "Bit wise AND (12 & 25) = " << (12 & 25) << endl;
    cout << "Bit logic AND (12 && 25) = " << (12 && 25) << endl;
    cout << "Bit wise OR (12 | 25) = " << (12 | 25) << endl;
    cout << "Bit wise OR (12 || 25) = " << (12 || 25) << endl;
    return 0;
}